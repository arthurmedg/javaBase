public class Produtor {
    private int idDiretor;
    private String nome;
    private String especialidade;
    private int anosDeExperiencia;
    private int numerosDeFilmesProduzidos;
    private String premiosRecebidos;

    public int getIdDiretor() {
        return idDiretor;
    }

    public void setIdDiretor(int idDiretor) {
        this.idDiretor = idDiretor;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getEspecialidade() {
        return especialidade;
    }

    public void setEspecialidade(String especialidade) {
        this.especialidade = especialidade;
    }

    public int getAnosDeExperiencia() {
        return anosDeExperiencia;
    }

    public void setAnosDeExperiencia(int anosDeExperiencia) {
        this.anosDeExperiencia = anosDeExperiencia;
    }

    public int getNumerosDeFilmesProduzidos() {
        return numerosDeFilmesProduzidos;
    }

    public void setNumerosDeFilmesProduzidos(int numerosDeFilmesProduzidos) {
        this.numerosDeFilmesProduzidos = numerosDeFilmesProduzidos;
    }

    public String getPremiosRecebidos() {
        return premiosRecebidos;
    }

    public void setPremiosRecebidos(String premiosRecebidos) {
        this.premiosRecebidos = premiosRecebidos;
    }
}
