public class Cena {
    private int idFilme;
    private String titulo;
    private int numeroCena;
    private double duracao;
    private String localGravacao;
    private String atoresEnvolvidos;

    public int getIdFilme() {
        return idFilme;
    }

    public void setIdFilme(int idFilme) {
        this.idFilme = idFilme;
    }

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    public int getNumeroCena() {
        return numeroCena;
    }

    public void setNumeroCena(int numeroCena) {
        this.numeroCena = numeroCena;
    }

    public double getDuracao() {
        return duracao;
    }

    public void setDuracao(double duracao) {
        this.duracao = duracao;
    }

    public String getLocalGravacao() {
        return localGravacao;
    }

    public void setLocalGravacao(String localGravacao) {
        this.localGravacao = localGravacao;
    }

    public String getAtoresEnvolvidos() {
        return atoresEnvolvidos;
    }

    public void setAtoresEnvolvidos(String atoresEnvolvidos) {
        this.atoresEnvolvidos = atoresEnvolvidos;
    }
}
