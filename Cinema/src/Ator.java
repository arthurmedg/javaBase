public class Ator {
    private int idAtor;
    private String nome;
    private int idade;
    private String nacionalidade;
    private int numeroDeFilmes;
    private String papelAtual;



//    getter & setter
    public int getIdAtor() {
        return idAtor;
    }

    public void setIdAtor(int idAtor) {
        this.idAtor = idAtor;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }

    public String getNacionalidade() {
        return nacionalidade;
    }

    public void setNacionalidade(String nacionalidade) {
        this.nacionalidade = nacionalidade;
    }

    public int getNumeroDeFilmes() {
        return numeroDeFilmes;
    }

    public void setNumeroDeFilmes(int numeroDeFilmes) {
        this.numeroDeFilmes = numeroDeFilmes;
    }

    public String getPapelAtual() {
        return papelAtual;
    }

    public void setPapelAtual(String papelAtual) {
        this.papelAtual = papelAtual;
    }
}
