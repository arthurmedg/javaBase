public class EngenheiroDeSom {
    private int idEngenheiroDeSom;
    private String nome;
    private String certificados;
    private int anosDeExperiencia;
    private String softwaresDominados;
    private int numeroDeProjetosRealizados;

    public int getIdEngenheiroDeSom() {
        return idEngenheiroDeSom;
    }

    public void setIdEngenheiroDeSom(int idEngenheiroDeSom) {
        this.idEngenheiroDeSom = idEngenheiroDeSom;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getCertificados() {
        return certificados;
    }

    public void setCertificados(String certificados) {
        this.certificados = certificados;
    }

    public int getAnosDeExperiencia() {
        return anosDeExperiencia;
    }

    public void setAnosDeExperiencia(int anosDeExperiencia) {
        this.anosDeExperiencia = anosDeExperiencia;
    }

    public String getSoftwaresDominados() {
        return softwaresDominados;
    }

    public void setSoftwaresDominados(String softwaresDominados) {
        this.softwaresDominados = softwaresDominados;
    }

    public int getNumeroDeProjetosRealizados() {
        return numeroDeProjetosRealizados;
    }

    public void setNumeroDeProjetosRealizados(int numeroDeProjetosRealizados) {
        this.numeroDeProjetosRealizados = numeroDeProjetosRealizados;
    }
}
