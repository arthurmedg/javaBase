public class Diretor {
    private int idDiretor;
    private String nome;
    private int anosDeExperiencia;
    private String estiloCinemetografico;
    private int numeroDeFilmes;
    private String premiosRecebidos;

    public int getIdDiretor() {
        return idDiretor;
    }

    public void setIdDiretor(int idDiretor) {
        this.idDiretor = idDiretor;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public int getAnosDeExperiencia() {
        return anosDeExperiencia;
    }

    public void setAnosDeExperiencia(int anosDeExperiencia) {
        this.anosDeExperiencia = anosDeExperiencia;
    }

    public String getEstiloCinemetografico() {
        return estiloCinemetografico;
    }

    public void setEstiloCinemetografico(String estiloCinemetografico) {
        this.estiloCinemetografico = estiloCinemetografico;
    }

    public int getNumeroDeFilmes() {
        return numeroDeFilmes;
    }

    public void setNumeroDeFilmes(int numeroDeFilmes) {
        this.numeroDeFilmes = numeroDeFilmes;
    }

    public String getPremiosRecebidos() {
        return premiosRecebidos;
    }

    public void setPremiosRecebidos(String premiosRecebidos) {
        this.premiosRecebidos = premiosRecebidos;
    }
}
